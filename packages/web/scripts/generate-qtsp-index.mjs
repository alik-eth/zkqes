#!/usr/bin/env node
// Pre-build hook — emits `src/generated/qtsp-index.ts` and (optionally)
// mirrors per-QTSP runtime data into `public/qtsp-data/` BEFORE
// `tsc -b` runs, so consumer modules (CountryGrid, qtspPage) find
// their import target.
//
// In dev mode (`pnpm dev`) the Vite plugin's `buildStart` hook regenerates
// the same artifacts every server start so dropping a new `meta.json`
// triggers a hot reload via `addWatchFile`. The plugin and this script
// implement the same logic; behaviour is identical between `pnpm dev`
// and `pnpm build`. This script only exists to satisfy the `tsc -b`
// pre-vite ordering inside the existing `pnpm build` command.
//
// Why duplicate the plugin's logic in `.mjs` instead of importing the
// `.ts` file? The plugin is TypeScript and Vite-shaped; running it
// from a node shell without a TS loader (tsx, ts-node) would either
// add a build-time dep or rely on Node's `--experimental-strip-types`
// flag (brittle across pnpm-store-cached node versions). ~50 lines of
// duplication is the lesser cost; the unit suite covers the plugin's
// behavior, and any logic drift between this and the plugin would
// show up immediately in the build vs dev runtime parity.
//
// Anchor: docs/superpowers/plans/2026-05-05-multi-qtsp-facade.md §T10.

import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  statSync,
  writeFileSync,
} from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { QtspMetaSchema } from '@zkqes/sdk/country/qtspMeta';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '../../..');
const trustRoot = resolve(repoRoot, 'fixtures/trust');
const outFile = resolve(here, '../src/generated/qtsp-index.ts');
const publicDataDir = resolve(here, '../public/qtsp-data');

function readSubdirs(dir) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir).filter((name) => {
    try {
      return statSync(join(dir, name)).isDirectory();
    } catch {
      return false;
    }
  });
}

function copyQtspRuntimeData(srcDir, destDir) {
  mkdirSync(destDir, { recursive: true });
  const samplesSrc = join(srcDir, 'samples.json');
  if (existsSync(samplesSrc)) {
    copyFileSync(samplesSrc, join(destDir, 'samples.json'));
  }
  const intermediatesSrc = join(srcDir, 'intermediates');
  if (existsSync(intermediatesSrc)) {
    const intermediatesDest = join(destDir, 'intermediates');
    mkdirSync(intermediatesDest, { recursive: true });
    for (const name of readdirSync(intermediatesSrc)) {
      if (!name.endsWith('.pem')) continue;
      copyFileSync(
        join(intermediatesSrc, name),
        join(intermediatesDest, name),
      );
    }
  }
}

const entries = [];
const seen = new Set();

for (const cc of readSubdirs(trustRoot)) {
  const ccDir = join(trustRoot, cc);
  for (const slug of readSubdirs(ccDir)) {
    const metaPath = join(ccDir, slug, 'meta.json');
    if (!existsSync(metaPath)) continue;

    let raw;
    try {
      raw = JSON.parse(readFileSync(metaPath, 'utf8'));
    } catch (e) {
      throw new Error(`qtsp-index: parse failed at ${metaPath}: ${e.message}`);
    }
    const result = QtspMetaSchema.safeParse(raw);
    if (!result.success) {
      throw new Error(
        `qtsp-index: schema fail at ${metaPath} (${cc}/${slug}): ${result.error.message}`,
      );
    }
    const key = `${result.data.country}/${result.data.qtspSlug}`;
    if (seen.has(key)) {
      throw new Error(
        `qtsp-index: duplicate (country, qtspSlug) ${key} — second occurrence at ${metaPath}`,
      );
    }
    seen.add(key);
    entries.push(result.data);

    copyQtspRuntimeData(
      join(ccDir, slug),
      join(publicDataDir, result.data.country, result.data.qtspSlug),
    );
  }
}

entries.sort((a, b) =>
  a.country !== b.country
    ? a.country.localeCompare(b.country)
    : a.qtspSlug.localeCompare(b.qtspSlug),
);

const out = [
  '// AUTO-GENERATED by qtspIndexPlugin — do not edit by hand.',
  '// Source of truth: fixtures/trust/<cc>/<slug>/meta.json.',
  "import type { QtspMeta } from '@zkqes/sdk';",
  '',
  'export const QTSP_INDEX: ReadonlyArray<QtspMeta> = Object.freeze([',
  ...entries.map((e) => `  ${JSON.stringify(e)},`),
  ']);',
  '',
].join('\n');

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, out);
process.stdout.write(`qtsp-index: emitted ${outFile} (${entries.length} entries)\n`);
