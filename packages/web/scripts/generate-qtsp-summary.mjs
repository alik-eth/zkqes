#!/usr/bin/env node
// Build-time: derive a per-QTSP summary from public/trusted-cas/trusted-cas.json
// + the curated QTSP_INDEX. Output src/generated/qtsp-summary.ts consumed by
// the /qtsps directory route. Re-run via `pnpm -F @zkqes/web build` (prebuild
// hook).

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(here, '..');

const TRUSTED_CAS = resolve(ROOT, 'public/trusted-cas/trusted-cas.json');

// OID 1.2.840.10045.3.1.7 = secp256r1 (NIST P-256).
// DER: 06 08 2A 86 48 CE 3D 03 01 07
const P256_OID_HEX = '06082a8648ce3d030107';
// OID 1.2.840.10045.2.1 = id-ecPublicKey
const EC_OID_HEX = '06072a8648ce3d0201';
// OID 1.2.840.113549.1.1.1 = rsaEncryption
const RSA_OID_HEX = '06092a864886f70d010101';

function detectKeyAlgs(certDerB64) {
  const hex = Buffer.from(certDerB64, 'base64').toString('hex');
  const isP256 = hex.includes(P256_OID_HEX);
  const isEc = hex.includes(EC_OID_HEX);
  const isRsa = hex.includes(RSA_OID_HEX);
  return { isP256, isEc, isRsa };
}

const tcs = JSON.parse(readFileSync(TRUSTED_CAS, 'utf8'));

// Group by (territory, tspName).
const groups = new Map();
for (const ca of tcs.cas) {
  const key = `${ca.territory}|${ca.tspName}`;
  let g = groups.get(key);
  if (!g) {
    g = {
      country: ca.territory,
      tspName: ca.tspName,
      certCount: 0,
      services: new Set(),
      p256: false,
      hasEc: false,
      hasRsa: false,
      latestServiceFrom: 0,
    };
    groups.set(key, g);
  }
  g.certCount += 1;
  g.services.add(ca.serviceName);
  const algs = detectKeyAlgs(ca.certDerB64);
  if (algs.isP256) g.p256 = true;
  if (algs.isEc) g.hasEc = true;
  if (algs.isRsa) g.hasRsa = true;
  if (ca.serviceValidFrom && ca.serviceValidFrom > g.latestServiceFrom) {
    g.latestServiceFrom = ca.serviceValidFrom;
  }
}

const rows = [...groups.values()]
  .map((g) => ({
    country: g.country,
    tspName: g.tspName,
    certCount: g.certCount,
    serviceCount: g.services.size,
    p256: g.p256,
    keyAlgs: [
      g.p256 ? 'ECDSA P-256' : null,
      g.hasEc && !g.p256 ? 'ECDSA (other)' : null,
      g.hasRsa ? 'RSA' : null,
    ].filter(Boolean),
    latestServiceFrom: g.latestServiceFrom,
  }))
  .sort((a, b) => a.country.localeCompare(b.country) || a.tspName.localeCompare(b.tspName));

const out = `// AUTO-GENERATED by scripts/generate-qtsp-summary.mjs — do not edit by hand.
// Source of truth: public/trusted-cas/trusted-cas.json (LOTL flattener output).

export interface QtspSummary {
  readonly country: string;
  readonly tspName: string;
  readonly certCount: number;
  readonly serviceCount: number;
  /** True when at least one CA in this TSP's chain advertises secp256r1 (P-256). */
  readonly p256: boolean;
  readonly keyAlgs: ReadonlyArray<string>;
  readonly latestServiceFrom: number;
}

export const QTSP_SUMMARY: ReadonlyArray<QtspSummary> = Object.freeze(${JSON.stringify(rows, null, 2)} as const);

export const QTSP_SUMMARY_META = Object.freeze({
  lotlSnapshot: ${JSON.stringify(tcs.lotlSnapshot)},
  trustDomain: ${JSON.stringify(tcs.trustDomain)},
  trustSources: ${JSON.stringify(tcs.trustSources)},
  totalCas: ${tcs.cas.length},
  totalTsps: ${rows.length},
});
`;

const OUT = resolve(ROOT, 'src/generated/qtsp-summary.ts');
writeFileSync(OUT, out);

const p256Count = rows.filter((r) => r.p256).length;
console.log(
  `qtsp-summary: emitted ${OUT} (${rows.length} TSPs, ${p256Count} with P-256, ${tcs.cas.length} CAs)`,
);
